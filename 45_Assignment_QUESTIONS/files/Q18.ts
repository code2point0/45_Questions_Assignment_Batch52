let places = ["London","Califonia","Dubai","Lahore","Karachi"];
for(let i=0;i<places.length;i++)
{
    console.log("Place That i want to visit :"+places[i]);
}
console.log("Coverting it to UpperCase");
for(let i=0;i<places.length;i++)
{
    console.log("Place That i want to visit :"+places[i].toUpperCase());
}
console.log("Reverse Order");
for(let i=(places.length-1);i>=0;i--)
{
    console.log("Place That i want to visit :"+places[i].toUpperCase());
}